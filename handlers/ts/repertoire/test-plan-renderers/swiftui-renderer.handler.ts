import type { FunctionalConceptHandler } from '../../../../runtime/functional-handler.ts';
import {
  createProgram, complete,
  type StorageProgram,
} from '../../../../runtime/storage-program.ts';
import { autoInterpret } from '../../../../runtime/functional-compat.ts';

type Result = { variant: string; [key: string]: unknown };

const CAPABILITIES = [
  'xctest',
  'view-inspector',
  'XCTAssertEqual',
  'async-await',
  'immediately',
  'eventually',
].join(',');

const _handler: FunctionalConceptHandler = {
  register(_input: Record<string, unknown>) {
    return complete(createProgram(), 'ok', {
      name: 'swiftui',
      capabilities: CAPABILITIES,
    }) as StorageProgram<Result>;
  },

  render(input: Record<string, unknown>) {
    const planStr = (input.plan as string | undefined) ?? '';
    if (!planStr || planStr.trim() === '') {
      return complete(createProgram(), 'error', {
        message: 'plan is required',
      }) as StorageProgram<Result>;
    }

    let plan: Record<string, unknown>;
    try {
      plan = JSON.parse(planStr);
    } catch (err) {
      return complete(createProgram(), 'error', {
        message: `plan is not valid JSON: ${err instanceof Error ? err.message : String(err)}`,
      }) as StorageProgram<Result>;
    }

    if (!plan || typeof plan !== 'object' || !plan.planId) {
      return complete(createProgram(), 'error', {
        message: 'plan is missing required field: planId',
      }) as StorageProgram<Result>;
    }

    const planId = plan.planId as string;
    const specKind = (plan.specKind as string) ?? 'unknown';

    // TODO: implement — replace this stub with the full probe translation
    // table for SwiftUI / XCTest / ViewInspector. See PRD §INV-12 for the
    // universal probe namespace spec. Key mappings to implement:
    //   body.textContent → try sut.inspect().find(ViewType.Text.self).string()
    //   state.<field>    → XCTAssertEqual(sut.state.<field>, expected)
    //   .focus()         → (SwiftUI accessibility focus APIs or UIKit fallback)
    //   immediately      → XCTAssertEqual(...)
    //   eventually       → expectation + waitForExpectations(timeout:)
    const code = `// Generated by SwiftUIRenderer — stub implementation (INV-12)
// TODO: SwiftUI renderer not yet implemented. Replace this file with the
// full probe translation once SwiftUIRenderer is fleshed out.
// Plan: ${planId}  SpecKind: ${specKind}

import XCTest
import ViewInspector

final class ${planId.replace(/[^a-zA-Z0-9]/g, '_')}Tests: XCTestCase {
    func test_stubNotYetImplemented() throws {
        // TODO: implement probe translation for ${specKind} spec.
        // See docs/plans/invariant-grammar-portability-prd.md §INV-12
        XCTAssertTrue(true, "SwiftUI renderer not yet implemented")
    }
}
`;

    return complete(createProgram(), 'ok', { code }) as StorageProgram<Result>;
  },
};

export const swiftUIRendererHandler = autoInterpret(_handler);
export default swiftUIRendererHandler;
