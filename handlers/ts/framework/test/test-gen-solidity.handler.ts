// @clef-handler style=imperative
// ============================================================
// TestGenSolidity Provider — Functional Style
//
// Renders a language-neutral TestPlan into Solidity test code
// using Foundry for fuzz tests and Echidna for invariant tests.
//
// See Architecture doc Sections 7.1, 7.2
// ============================================================

import type { FunctionalConceptHandler } from '../../../../runtime/functional-handler.ts';
import {
  createProgram, put, find, complete,
  type StorageProgram,
} from '../../../../runtime/storage-program.ts';
import type { TestPlan, TestPlanAction } from './test-gen.handler.ts';

const RESULTS = 'test-gen-solidity-results';

type Result = { variant: string; [key: string]: unknown };

let idCounter = 0;
function nextId(): string {
  return `tgsol-${++idCounter}`;
}

export function resetTestGenSolidityCounter(): void {
  idCounter = 0;
}

function toPascal(name: string): string {
  return name.charAt(0).toUpperCase() + name.slice(1);
}

function renderSolidityTests(plan: TestPlan): string {
  const contractName = `${toPascal(plan.conceptName)}ConformanceTest`;
  const targetContract = toPascal(plan.conceptName);
  const lines: string[] = [];

  lines.push('// SPDX-License-Identifier: MIT');
  lines.push('// Auto-generated by TestGenSolidity from concept spec invariants.');
  lines.push('pragma solidity ^0.8.20;');
  lines.push('');
  lines.push('import "forge-std/Test.sol";');
  lines.push(`import "../src/${targetContract}.sol";`);
  lines.push('');
  lines.push(`contract ${contractName} is Test {`);
  lines.push(`    ${targetContract} internal target;`);
  lines.push('');
  lines.push('    function setUp() public {');
  lines.push(`        target = new ${targetContract}();`);
  lines.push('    }');
  lines.push('');

  // Structural tests per action
  for (const action of plan.actions) {
    renderSolidityActionTest(lines, action);
  }

  // Example conformance tests
  for (const example of plan.examples) {
    const fnName = example.name.replace(/[^a-zA-Z0-9]/g, '_');
    lines.push(`    function test_example_${fnName}() public {`);
    for (const step of example.steps) {
      lines.push(`        // ${step.action}(${JSON.stringify(step.input)})`);
      lines.push(`        // Expected variant: ${step.expectedVariant}`);
    }
    lines.push('    }');
    lines.push('');
  }

  // Forall / fuzz tests with vm.assume constraints
  for (const prop of plan.properties) {
    const fnName = prop.name.replace(/[^a-zA-Z0-9]/g, '_');

    // Build typed fuzz parameters from quantifiers
    const fuzzParams: string[] = [];
    for (const q of prop.quantifiers) {
      const t = (q.fieldName || '').toLowerCase();
      if (t === 'int' || t === 'number') {
        fuzzParams.push(`uint256 ${q.variable}`);
      } else if (t === 'bool' || t === 'boolean') {
        fuzzParams.push(`bool ${q.variable}`);
      } else {
        fuzzParams.push(`string memory ${q.variable}`);
      }
    }
    if (fuzzParams.length === 0) {
      fuzzParams.push('string memory input');
    }

    lines.push(`    function testFuzz_${fnName}(${fuzzParams.join(', ')}) public {`);

    // Add vm.assume constraints for set_literal domains
    for (const q of prop.quantifiers) {
      if (q.domainType === 'set_literal' && q.values) {
        lines.push(`        // Constrain ${q.variable} to valid domain`);
        const conditions = q.values.map(v =>
          `keccak256(bytes(${q.variable})) == keccak256(bytes("${v}"))`
        ).join(' || ');
        lines.push(`        vm.assume(${conditions});`);
      }
    }

    lines.push(`        // Property: ${prop.name}`);
    for (const step of prop.steps) {
      lines.push(`        // target.${step.action}(...)`);
    }
    lines.push('        assertTrue(true);');
    lines.push('    }');
    lines.push('');
  }

  // Always invariants (Echidna-style)
  for (const inv of plan.stateInvariants) {
    if (inv.kind === 'always') {
      const fnName = inv.name.replace(/[^a-zA-Z0-9]/g, '_');
      lines.push(`    function invariant_${fnName}() public view {`);
      lines.push(`        // Always: ${inv.name}`);
      lines.push('        assertTrue(true);');
      lines.push('    }');
      lines.push('');
    } else {
      const fnName = inv.name.replace(/[^a-zA-Z0-9]/g, '_');
      lines.push(`    function test_never_${fnName}() public {`);
      lines.push(`        // Never: ${inv.name}`);
      lines.push('        assertTrue(true);');
      lines.push('    }');
      lines.push('');
    }
  }

  // Contract tests
  for (const contract of plan.contracts) {
    for (const pre of contract.preconditions) {
      const fnName = `${contract.targetAction}_requires_${pre.assertion}`.replace(/[^a-zA-Z0-9]/g, '_');
      lines.push(`    function test_contract_${fnName}() public {`);
      lines.push(`        // Precondition: ${pre.assertion}`);
      lines.push(`        vm.expectRevert();`);
      lines.push(`        // target.${contract.targetAction}();`);
      lines.push('    }');
      lines.push('');
    }
  }

  lines.push('}');
  lines.push('');

  return lines.join('\n');
}

function renderSolidityActionTest(lines: string[], action: TestPlanAction): void {
  lines.push(`    function test_${action.name}_returns_valid() public {`);
  lines.push(`        // Action: ${action.name}`);
  lines.push(`        // Variants: ${action.variants.join(', ')}`);
  lines.push('        assertTrue(true);');
  lines.push('    }');
  lines.push('');
}

export const testGenSolidityHandler: FunctionalConceptHandler = {

  render(input: Record<string, unknown>) {
    const testPlanJson = input.test_plan as string;
    const outputPath = input.output_path as string;

    if (!testPlanJson || !outputPath) {
      return complete(createProgram(), 'invalid', { message: 'test_plan and output_path are required' }) as StorageProgram<Result>;
    }

    let plan: TestPlan;
    try { plan = JSON.parse(testPlanJson); } catch {
      return complete(createProgram(), 'invalid', { message: 'Failed to parse test_plan JSON' }) as StorageProgram<Result>;
    }

    if (!plan.conceptName || !plan.actions) {
      return complete(createProgram(), 'invalid', { message: 'TestPlan missing required fields' }) as StorageProgram<Result>;
    }

    const renderedCode = renderSolidityTests(plan);
    const testCount = (renderedCode.match(/function test/g) || []).length;
    const id = nextId();

    let p = createProgram();
    p = put(p, RESULTS, id, {
      id, test_plan: testPlanJson, rendered_code: renderedCode,
      file_path: outputPath, rendered_at: new Date().toISOString(), test_count: testCount,
    });

    return complete(p, 'ok', {
      result: id, rendered_code: renderedCode, file_path: outputPath, test_count: testCount,
    }) as StorageProgram<Result>;
  },

  renderBatch(input: Record<string, unknown>) {
    const plansJson = input.test_plans as string;
    if (!plansJson) return complete(createProgram(), 'error', { message: 'test_plans required' }) as StorageProgram<Result>;

    let items: Array<{ test_plan: string; output_path: string }>;
    try { items = JSON.parse(plansJson); } catch {
      return complete(createProgram(), 'error', { message: 'Invalid JSON' }) as StorageProgram<Result>;
    }

    let p = createProgram();
    let totalTests = 0;
    const results: Array<{ file_path: string; test_count: number }> = [];
    for (const item of items) {
      let plan: TestPlan;
      try { plan = JSON.parse(item.test_plan); } catch { continue; }
      if (!plan.conceptName || !plan.actions) continue;
      const code = renderSolidityTests(plan);
      const tc = (code.match(/function test/g) || []).length;
      const id = nextId();
      p = put(p, RESULTS, id, { id, rendered_code: code, file_path: item.output_path, test_count: tc });
      results.push({ file_path: item.output_path, test_count: tc });
      totalTests += tc;
    }
    return complete(p, 'ok', { results: JSON.stringify(results), total_files: results.length, total_tests: totalTests }) as StorageProgram<Result>;
  },

  listRendered(input: Record<string, unknown>) {
    void input;
    let p = createProgram();
    p = find(p, RESULTS, {}, 'allResults');
    return complete(p, 'ok', { results: '[]' }) as StorageProgram<Result>;
  },
};
